#include "ft_printf.h"

int	p_uint(unsigned int n)
{
	int		len;
	char	c;

	len = 0;
	if (n >= 10)
		len += p_uint(n / 10);
	c = (char)('0' + (n % 10));
	len += (int)write(1, &c, 1);
	return (len);
}
